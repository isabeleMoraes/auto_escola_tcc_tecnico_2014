    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        ExameDao dao = new ExameDao();
        
        if(pesquisar.getText().isEmpty()){
            pesquisar.setText("0");
        }
        ArrayList<Exame> exames = dao.listarExameAluno(pesquisar.getText());

        if(exames.isEmpty()){
            JOptionPane.showMessageDialog(this, "Nenhuma aula encontrada");
            pesquisar.setText("");
        }else{
            tabela = new TableModelExames(exames);
            jtExame.setModel(tabela);
        }
        pesquisar.setText("");
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void jtExameMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtExameMouseClicked
        if(evt.getClickCount() == 2){
            int linhaSelecionada = jtExame.getSelectedRow();
            tabela = (TableModelExames) jtExame.getModel();
            Exame ex = tabela.retornarExameSelecionada(linhaSelecionada);
            int b = 2;
            CadastrarExame frm = new CadastrarExame(this, ex, b);
            frm.alterarExame = true;
            frm.setVisible(true);
            frm.setAlwaysOnTop(true);
            frm.setLocationRelativeTo(null);
        }
    }//GEN-LAST:event_jtExameMouseClicked

